#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	i;

	i = 0;
	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (nb < 0)
	{
		nb *= -1;
		write(1, "-", 1);
	}
	if (nb >= 10)
		ft_putnbr(nb / 10);
	i = (nb % 10) + '0';
	write(1, &i, 1);
}
