void	ft_putnbr(int nb)
{
	int	temp;

	temp = nb;
	temp += '0';
	if (nb < 0)
	{
		write(1, "-", 1);
		nb *= -1;
	}
	if (nb >= 10)
	{
		temp = nb % 10;
		nb = nb / 10;
		temp += '0';
		ft_putnbr(nb);
	}
	write(1, &temp, 1);
}
