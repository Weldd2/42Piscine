int	ft_atoi(char *str)
{
	int	r;
	int	signe;

	r = 0;
	signe = 1;
	while (*str == ' ' || (*str >= 7 && *str <= 13))
		str++;
	while (*str == '+' || *str == '-')
	{
		if (*str == '-')
			signe = -signe;
		str++;
	}
	while (*str <= '9' && *str >= '0')
	{
		r *= 10;
		r += (*str - '0');
		str++;
	}
	return (r * signe);
}
